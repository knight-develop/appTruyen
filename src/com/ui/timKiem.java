    private List<Truyen> role(){      
        String name = txtname.getText();
        List<Truyen> listTR = new ArrayList<>();
        
        if(flag == true){          
            try {
                theLoai tl = tDAO.getIDByName(name);
                if(tl == null){
                    return null;
                }
                List<chiTietTruyen> list = cTDAO.selectListByIDTL(tl.getId());
                if(list == null){
                    return null;
                }
                for(chiTietTruyen ct : list){
                    Truyen tr = trDA0.selectByID(ct.getTruyen());
                    listTR.add(tr);
                }
                
            } catch (SQLException ex) {
                Logger.getLogger(timKiem.class.getName()).log(Level.SEVERE, null, ex);
            }
        }else{
            Truyen tr = trDA0.selectByName(name);
            if(tr == null){
                return null;
            }
            listTR.add(tr);
        }
        return listTR;
    }
    private void fill() {
        DefaultListModel model = (DefaultListModel) listTruyen.getModel();
        model.clear();
        List<Truyen> list = role();
        if(list == null){
            return;
        }
        for (Truyen tr :list) {
            model.addElement(new Truyen(tr.getName(), new ImageIcon("E:\\DuAn1\\appDocTruyen\\src\\com\\imgStory\\" + tr.getHinh())));
        }
        listTruyen.setCellRenderer(new Renderer());
        listTruyen.setModel(model);
    }

